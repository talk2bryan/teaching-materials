 /* A very simple class that encapsulates an (x,y) pair 
  * of coordinates as a single object, for convenience. 
  * Everything is public. There is only a constructor. 
  */
public class Point {
  public double x, y; 
  
  public Point(double x, double y) {
    this.x = x;
    this.y = y;
  } // constructor
} // Point class